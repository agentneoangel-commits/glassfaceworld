        function openModal(project, youtubeId) {
            const modal = document.getElementById('modal');
            if (!modal) return;

            document.getElementById('modalTitle').textContent = project.title;
            document.getElementById('modalRole').textContent = project.artist + (project.featured ? ' ★' : '');
            document.getElementById('modalText').textContent = project.description || 'No description available.';

            const mediaEl = document.getElementById('modalMedia');
            if (youtubeId) {
                mediaEl.innerHTML = `
                    <iframe 
                        src="https://www.youtube.com/embed/${youtubeId}?rel=0" 
                        title="${project.title}"
                        frameborder="0" 
                        allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" 
                        allowfullscreen>
                    </iframe>
                `;
            } else {
                const mappedCategory = CATEGORY_MAP[project.category] || project.category;
            const icon = CATEGORY_ICONS[mappedCategory] || CATEGORY_ICONS[project.category] || '◆';
                mediaEl.innerHTML = `
                    <div style="background: var(--bg-secondary); display: flex; align-items: center; justify-content: center; aspect-ratio: 16/9; font-size: 64px;">
                        ${icon}
                    </div>
                `;
            }

            const statsEl = document.getElementById('modalStats');
            const year = new Date(project.publishedAt).getFullYear();
            
            if (project.viewCount && project.viewCount >= 1000000) {
                statsEl.innerHTML = `
                    <div class="stat-item">
                        <div class="stat-value">${formatPlayCount(project.viewCount)}</div>
                        <div class="stat-label">YouTube Views</div>
                    </div>
                    <div class="stat-item">
                        <div class="stat-value">${year}</div>
                        <div class="stat-label">Year</div>
                    </div>
                `;
            } else {
                statsEl.innerHTML = `
                    <div class="stat-item">
                        <div class="stat-value">${year}</div>
                        <div class="stat-label">Year</div>
                    </div>
                `;
            }
            statsEl.style.display = 'flex';

            modal.classList.add('active');
            document.body.style.overflow = 'hidden';
        }

        function closeModal() {
            const modal = document.getElementById('modal');
            if (!modal) return;

            modal.classList.remove('active');
            document.body.style.overflow = '';

            const mediaEl = document.getElementById('modalMedia');
            if (mediaEl) {
                mediaEl.innerHTML = '';
            }
        }

        document.getElementById('modal')?.addEventListener('click', (e) => {
            if (e.target.classList.contains('modal')) {
                closeModal();
            }
        });

        document.addEventListener('keydown', (e) => {
            if (e.key === 'Escape') {
                closeModal();
            }
        });


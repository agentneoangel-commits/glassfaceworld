        // ========================================
        // 3D CLOUD HOVER EFFECT - SPATIAL INTELLIGENCE + Z-ROTATION
        // ========================================
        function setup3DCloudHover() {
            const content = document.getElementById('timelineContent');
            if (!content) return;

            content.addEventListener('mouseover', (e) => {
                const hoveredCard = e.target.closest('.project-card');
                if (!hoveredCard) return;

                const allCards = Array.from(content.querySelectorAll('.project-card'));
                const hoveredRect = hoveredCard.getBoundingClientRect();
                const hoveredCenterX = hoveredRect.left + hoveredRect.width / 2;
                const hoveredCenterY = hoveredRect.top + hoveredRect.height / 2;

                // Mark hovered card - POP FORWARD
                hoveredCard.classList.add('hovered');
                hoveredCard.style.transform = 'translate(-50%, -50%) scale(1.5) translateZ(100px)';
                hoveredCard.style.opacity = '1';
                hoveredCard.style.filter = 'none';
                hoveredCard.style.zIndex = '100';

                // Calculate distance from hovered card for each card
                allCards.forEach((card) => {
                    if (card === hoveredCard) return;

                    const cardRect = card.getBoundingClientRect();
                    const cardCenterX = cardRect.left + cardRect.width / 2;
                    const cardCenterY = cardRect.top + cardRect.height / 2;

                    // Calculate Euclidean distance
                    const distanceX = cardCenterX - hoveredCenterX;
                    const distanceY = cardCenterY - hoveredCenterY;
                    const distance = Math.sqrt(distanceX * distanceX + distanceY * distanceY);

                    // TIGHTER RADIUS - Only affect cards within 250px (about 2-3 card widths)
                    const effectRadius = 250;
                    
                    if (distance > effectRadius) {
                        // Far cards - fade to background
                        card.style.transform = 'translate(-50%, -50%) scale(0.7) translateZ(-100px)';
                        card.style.opacity = '0.2';
                        card.style.filter = 'brightness(0.4) grayscale(60%)';
                        card.style.zIndex = '1';
                        return;
                    }

                    // Normalize distance within effect radius (0 = at center, 1 = at edge)
                    const distanceFactor = distance / effectRadius;
                    
                    // INVERSE CURVE - closer cards get MORE effect
                    // Use ease-out curve for more dramatic near-hover effect
                    const effectStrength = Math.pow(1 - distanceFactor, 1.5);

                    // Scale: 1.3 (very close) -> 1.0 (mid) -> 0.8 (edge of radius)
                    const scale = 0.8 + (effectStrength * 0.5);

                    // Opacity: 1.0 (very close) -> 0.7 (mid) -> 0.4 (edge)
                    const opacity = 0.4 + (effectStrength * 0.6);

                    // Z-depth: closer = more forward, further = pushed back
                    const translateZ = (effectStrength * 40) - 30;

                    // Y-rotation (horizontal turn) - face toward/away from hover
                    const rotateY = (distanceX / effectRadius) * 25 * (distanceFactor > 0.5 ? 1 : -0.5);

                    // Z-ROTATION (tilt/roll) - MORE with distance, up to 15°
                    // Creates that "scattered cards" effect
                    const rotateZ = (distanceX / effectRadius) * 15 * distanceFactor;

                    // X-rotation (pitch) - slight vertical tilt
                    const rotateX = (distanceY / effectRadius) * 10 * distanceFactor;

                    // Push cards away from center - stronger push for closer cards
                    const pushDistance = 30 * effectStrength;
                    const pushX = (distanceX / (distance + 0.1)) * pushDistance;
                    const pushY = (distanceY / (distance + 0.1)) * pushDistance * 0.5;

                    // Apply combined 3D transform
                    card.style.transform = `
                        translate(calc(-50% + ${pushX}px), calc(-50% + ${pushY}px)) 
                        scale(${scale}) 
                        translateZ(${translateZ}px) 
                        rotateY(${rotateY}deg)
                        rotateX(${rotateX}deg)
                        rotateZ(${rotateZ}deg)
                    `;
                    card.style.opacity = opacity;
                    card.style.filter = `brightness(${0.6 + effectStrength * 0.4})`;
                    card.style.zIndex = Math.floor(50 + effectStrength * 50);
                });
            });

            content.addEventListener('mouseout', (e) => {
                const hoveredCard = e.target.closest('.project-card');
                if (!hoveredCard) return;

                // Check if we're actually leaving the card (not entering a child)
                if (hoveredCard.contains(e.relatedTarget)) return;

                // Reset all cards smoothly with transition
                const allCards = content.querySelectorAll('.project-card');
                allCards.forEach(card => {
                    card.classList.remove('hovered');
                    card.style.transform = 'translate(-50%, -50%) scale(0.85)';
                    card.style.opacity = '0.8';
                    card.style.filter = 'none';
                    card.style.zIndex = '';
                });
            });
        }

        function handleDragStart(e) {
            state.isDragging = true;
            state.startX = e.pageX;
            state.scrollLeft = getCurrentTranslateX();
            
            const container = document.getElementById('timelineContainer');
            container.classList.add('dragging');
        }

        function handleDragMove(e) {
            if (!state.isDragging) return;
            e.preventDefault();
            
            const x = e.pageX;
            const walk = (x - state.startX) * 1.5;
            setTranslateX(state.scrollLeft + walk);
        }

        function handleDragEnd() {
            state.isDragging = false;
            const container = document.getElementById('timelineContainer');
            container.classList.remove('dragging');
        }

        function handleTouchStart(e) {
            state.isDragging = true;
            state.startX = e.touches[0].pageX;
            state.scrollLeft = getCurrentTranslateX();
        }

        function handleTouchMove(e) {
            if (!state.isDragging) return;
            e.preventDefault();
            
            const x = e.touches[0].pageX;
            const walk = (x - state.startX) * 1.5;
            setTranslateX(state.scrollLeft + walk);
        }

        function handleWheel(e) {
            if (Math.abs(e.deltaX) > Math.abs(e.deltaY)) {
                return;
            }
            
            e.preventDefault();
            const currentX = getCurrentTranslateX();
            setTranslateX(currentX - e.deltaY);
        }

        function handleKeydown(e) {
            const step = 300;
            const currentX = getCurrentTranslateX();
            
            switch(e.key) {
                case 'ArrowLeft':
                    e.preventDefault();
                    animateToX(currentX + step);
                    break;
                case 'ArrowRight':
                    e.preventDefault();
                    animateToX(currentX - step);
                    break;
                case 'Home':
                    e.preventDefault();
                    centerOnYear(CONFIG.yearStart);
                    break;
                case 'End':
                    e.preventDefault();
                    centerOnYear(CONFIG.yearEnd);
                    break;
            }
        }

        // ========================================
        // TRANSFORM UTILITIES
        // ========================================
        function getCurrentTranslateX() {
            const content = document.getElementById('timelineContent');
            if (!content) return 0;
            
            const style = window.getComputedStyle(content);
            const matrix = new WebKitCSSMatrix(style.transform);
            return matrix.m41;
        }

        function setTranslateX(x) {
            const content = document.getElementById('timelineContent');
            if (!content) return;

            const containerWidth = document.getElementById('timelineContainer').offsetWidth;
            const maxScroll = -(state.totalWidth - containerWidth + 100);
            const minScroll = containerWidth / 2;

            x = Math.max(maxScroll, Math.min(minScroll, x));
            
            content.style.transform = `translateX(${x}px)`;
        }

        function animateToX(targetX) {
            const content = document.getElementById('timelineContent');
            if (!content) return;

            content.style.transition = 'transform 0.3s ease-out';
            setTranslateX(targetX);
            
            setTimeout(() => {
                content.style.transition = '';
            }, 300);
        }

        function centerOnYear(year) {
            const container = document.getElementById('timelineContainer');
            if (!container) return;

            const containerWidth = container.offsetWidth;
            const yearOffset = (year - CONFIG.yearStart) * CONFIG.pxPerYear;
            const targetX = (containerWidth / 2) - yearOffset;

            animateToX(targetX);
        }

        // ========================================
        // ZOOM CONTROLS
        // ========================================
        function zoomIn() {
            state.currentZoom = Math.min(state.currentZoom * 1.3, CONFIG.maxZoom);
            applyZoom();
        }

        function zoomOut() {
            state.currentZoom = Math.max(state.currentZoom / 1.3, CONFIG.minZoom);
            applyZoom();
        }

        function resetZoom() {
            state.currentZoom = 0.4;
            applyZoom();
            fitAllProjects();
        }

        function fitAllProjects() {
            // Calculate zoom level to fit all projects
            const container = document.getElementById('timelineContainer');
            if (!container) return;
            
            const containerWidth = container.offsetWidth;
            const yearSpan = CONFIG.yearEnd - CONFIG.yearStart + 1;
            
            // Add padding years on each side
            const targetWidth = containerWidth * 0.9;
            const zoomLevel = targetWidth / (yearSpan * 200);
            
            state.currentZoom = Math.max(0.25, Math.min(zoomLevel, 1));
            applyZoom();
            
            // Center the timeline
            const content = document.getElementById('timelineContent');
            if (content) {
                const totalWidth = yearSpan * CONFIG.pxPerYear;
                const startX = (containerWidth - totalWidth) / 2;
                content.style.transform = `translateX(${startX}px)`;
            }
        }

        function applyZoom() {
            const content = document.getElementById('timelineContent');
            if (!content) return;

            content.style.setProperty('--zoom', state.currentZoom);
            CONFIG.pxPerYear = 200 * state.currentZoom;
            
            renderTimeline();
        }


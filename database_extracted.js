        function renderTimeline() {
            const container = document.getElementById('timelineContent');
            if (!container) return;

            container.innerHTML = '';

            const yearSpan = CONFIG.yearEnd - CONFIG.yearStart + 1;
            state.totalWidth = yearSpan * CONFIG.pxPerYear;

            for (let year = CONFIG.yearStart; year <= CONFIG.yearEnd; year++) {
                const marker = document.createElement('div');
                marker.className = 'year-marker';
                marker.style.left = `${(year - CONFIG.yearStart) * CONFIG.pxPerYear}px`;
                marker.innerHTML = `<span class="year-label">${year}</span>`;
                container.appendChild(marker);
            }

            state.projects.forEach((project, index) => {
                if (!project.publishedAt) return;

                const card = createProjectCard(project, index);
                if (card) {
                    container.appendChild(card);
                }
            });
        }

        function createProjectCard(project, index) {
            const date = new Date(project.publishedAt);
            const year = date.getFullYear();
            const month = date.getMonth();
            
            if (year < CONFIG.yearStart || year > CONFIG.yearEnd) return null;

            const yearOffset = year - CONFIG.yearStart;
            
            // BETTER DISTRIBUTION: Use project hash + category for consistent positioning
            const idHash = project._id.split('').reduce((a, b) => a + b.charCodeAt(0), 0);
            const categoryHash = (project.category || '').split('').reduce((a, b) => a + b.charCodeAt(0), 0);
            
            // Spread horizontally with more space between projects
            // Use both ID hash and category to create distinct "lanes"
            const spreadOffset = ((idHash + categoryHash) % 100) / 100 * (CONFIG.pxPerYear * 0.7);
            const xPos = (yearOffset * CONFIG.pxPerYear) + 40 + spreadOffset; // Add 40px padding

            // Better vertical distribution - more bands for less overlap
            const verticalBands = 8; // INCREASED from 6
            const bandHeight = 10; // percentage per band (was 12%)
            const baseBand = (index + idHash) % verticalBands;
            const randomOffset = ((idHash % 20) - 10) / 5; // -2% to 2% random
            const yPos = 10 + (baseBand * bandHeight) + randomOffset;

            const card = document.createElement('div');
            card.className = 'project-card floating';
            card.dataset.category = CATEGORY_MAP[project.category] || project.category;
            card.dataset.projectId = project._id;
            
            const duration = CONFIG.floating.durationMin + Math.random() * (CONFIG.floating.durationMax - CONFIG.floating.durationMin);
            const delay = index * CONFIG.floating.stagger;
            const amplitude = CONFIG.floating.amplitude * (0.8 + Math.random() * 0.4);
            
            card.style.left = `${xPos}px`;
            card.style.top = `${yPos}%`;
            card.style.setProperty('--float-duration', `${duration}ms`);
            card.style.setProperty('--float-delay', `${delay}s`);
            card.style.setProperty('--float-offset', `-${amplitude}px`);

            const hasYouTube = project.externalUrl && project.externalUrl.includes('youtube');
            const youtubeId = hasYouTube ? extractYouTubeId(project.externalUrl) : null;
            const imgUrl = youtubeId ? `https://img.youtube.com/vi/${youtubeId}/hqdefault.jpg` : null;

            const hasViews = project.viewCount && project.viewCount >= 1000000;
            const viewsBadge = hasViews ? `<span class="project-views">${formatPlayCount(project.viewCount)}</span>` : '';

            const thumbnail = imgUrl
                ? `<img src="${imgUrl}" alt="${project.title}" class="project-image" onerror="this.style.display='none'; this.nextElementSibling.style.display='flex';">`
                : '';

            const placeholder = `
                <div class="project-placeholder" style="display: ${imgUrl ? 'none' : 'flex'}">
                    ${CATEGORY_ICONS[project.category] || '◆'}
                </div>
            `;

            card.innerHTML = `
                <div class="project-thumb">
                    ${thumbnail}
                    ${placeholder}
                    ${viewsBadge}
                </div>
                <div class="project-info">
                    <div class="project-title">${project.title}</div>
                    <div class="project-date">${formatDate(project.publishedAt)}</div>
                </div>
            `;

            card.addEventListener('click', () => openModal(project, youtubeId));

            return card;
        }

        function extractYouTubeId(url) {
            if (!url) return null;
            const match = url.match(/(?:youtube\.com\/watch\?v=|youtu\.be\/)([a-zA-Z0-9_-]+)/);
            return match ? match[1] : null;
        }

